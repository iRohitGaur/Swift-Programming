let name = "Steve"

let age = 40

let radio = 98.3

let radioFloat: Float = 98.3

print("name: \(name) \t\t datatype: \(type(of: name))")                 // name: Steve              datatype: String
print("age: \(age) \t\t datatype: \(type(of: age))")                    // age: 40                  datatype: Int
print("radio: \(radio) \t\t datatype: \(type(of: radio))")              // radio: 98.3              datatype: Double
print("radioFloat: \(radioFloat) \t datatype: \(type(of: radioFloat))") // radioFloat: 98.3         datatype: Float